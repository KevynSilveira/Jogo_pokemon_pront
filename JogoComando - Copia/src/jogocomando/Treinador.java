package jogocomando;
public class Treinador {
    
    //Atributos
    private String nome;
    private String secundario;
    
    //Construtor
    public Treinador() {
    }
    
    
    public String getNome() {
        return nome;
    }
    //get nome
    public void setNome(String nome) {
        this.nome = nome;
    }
    //set nome

    public String getSecundario() {
        return secundario;
    }

    public void setSecundario(String secundario) {
        this.secundario = secundario;
    }
    
   
}
